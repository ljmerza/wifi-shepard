def looks_like_espressif(mac: str) -> bool:
    """True if ``mac``'s OUI (first three octets) is a known Espressif block.

    Advisory only — a False result means "warn the operator," never "block."
    """
    oui = normalize_mac(mac)[:8]  # "aa:bb:cc" from a normalized colon-form MAC
    return oui in _ESPRESSIF_OUIS
