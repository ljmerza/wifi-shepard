"""Espressif OUI pre-filter (ADR-0005 Fork B / AC-5).

The OUI identifies the *chip vendor* only, never the firmware or reboot path, so
it is **never** an action trigger or an eligibility gate. Its sole use is an
advisory config-load warning: if an operator opts a MAC into ``reboot.eligible``
whose OUI is not a known Espressif block, it is likely a typo onto a laptop /
phone rather than an ESP-based IoT device. The opt-in is still honored — the
warning only nudges the operator to double-check.

Provenance of ``_ESPRESSIF_OUIS``:
  - Source:   IEEE OUI registry (MA-L assignments), ``/usr/share/ieee-data/oui.csv``.
  - Snapshot: 2024-07-22 (the local registry's last-update date).
  - Filter:   ``grep -i espressif`` over the registry → 198 unique OUIs.
  - Regen:    ``grep -i espressif /usr/share/ieee-data/oui.csv \
                | awk -F, '{print tolower($2)}' | sort -u``
              (re-run against a fresh registry, then reformat as colon-form).

Staleness note: this is a point-in-time snapshot. As IEEE allocates *new*
Espressif OUIs, an old list produces **false-positive** warnings on genuinely
Espressif devices (never false silences — it cannot wrongly suppress a warning
for a non-Espressif MAC). An operator seeing this warning on a known-good new
device is hitting a stale list, not a real misconfiguration; refresh via the
Regen command above.
"""

